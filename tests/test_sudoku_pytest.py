def test_anti_brute_force():
    input = [[i, i, i, i, i, i, i, i, i],
             [i, i, i, i, i, 3, i, 8, 5],
             [i, i, 1, i, 2, i, i, i, i],
             [i, i, i, 5, i, 7, i, i, i],
             [i, i, 4, i, i, i, 1, i, i],
             [i, 9, i, i, i, i, i, i, i],
             [5, i, i, i, i, i, i, 7, 3],
             [i, i, 2, i, 1, i, i, i, i],
             [i, i, i, i, 4, i, i, i, 9]]
    assert Sudoku.main(input) == [[9, 8, 7, 6, 5, 4, 3, 2, 1],
                                  [2, 4, 6, 1, 7, 3, 9, 8, 5],
                                  [3, 5, 1, 9, 2, 8, 7, 4, 6],
                                  [1, 2, 8, 5, 3, 7, 6, 9, 4],
                                  [6, 3, 4, 8, 9, 2, 1, 5, 7],
                                  [7, 9, 5, 4, 6, 1, 8, 3, 2],
                                  [5, 1, 9, 2, 8, 6, 4, 7, 3],
                                  [4, 7, 2, 3, 1, 9, 5, 6, 8],
                                  [8, 6, 3, 7, 4, 5, 2, 1, 9]]


